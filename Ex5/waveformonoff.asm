ORG 0000H
    MOV TMOD, #10H
    MOV P2, #00H
    SETB P2.3
BACK:
    MOV TH0, #0FFH
    MOV TL0, #076H
    SETB TR0
AGAIN:
    JNB TF0, AGAIN
    CLR TR0
    CLR TF0
    CLR P2.3
    MOV TH1, #0FCH
    MOV TL1, #0C1H
    SETB TR1
AGAIN1:
    JNB TF1, AGAIN1
    CLR TR1
    CLR TF1
    SETB P2.3
    SJMP BACK
END
